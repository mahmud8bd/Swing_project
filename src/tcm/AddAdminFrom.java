    public void dataShowTable() {
        try {
            con = MyConnection.getConnection();
            pst = con.prepareStatement("select * from admin");
            ResultSet set = pst.executeQuery();
            dft.setRowCount(0);
            while (set.next()) {

                Object[] data = {set.getString(2), set.getString(3), set.getString(4),
                    set.getString(5)};

                dft.addRow(data);
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }

