    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (veriText()) {
            if (passOne.getText().equals(PassCheck.getText())) {
                try {
                    con = MyConnection.getConnection();
                    pst = con.prepareStatement("insert into admin(fullname, username, contact, email, password) "
                            + "values(?,?,?,?,?)");
                    pst.setString(1, fullName.getText());
                    pst.setString(2, userName.getText());
                    pst.setString(3, contactNumber.getText());
                    pst.setString(4, email.getText());
                    pst.setString(5, passOne.getText());
                    int okOne = pst.executeUpdate();

                    if (okOne > 0) {
                        JOptionPane.showMessageDialog(this, "Added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Fail", "Fail", JOptionPane.ERROR_MESSAGE);

                    }
                    con.close();
                } catch (Exception e) {
                }
                fieldClean();
            }else{
               JOptionPane.showMessageDialog(this, "Repassword Not Match!", "Fail", JOptionPane.ERROR_MESSAGE); 
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            con = MyConnection.getConnection();
            pst = con.prepareStatement("update admin set fullname=?, username=?, "
                    + "contact=?, email=? Where username=?");
            pst.setString(1, fullName.getText());
            pst.setString(2, userName.getText());
            pst.setString(3, contactNumber.getText());
            pst.setString(4, email.getText());
            pst.setString(5, userName.getText());
            int query = pst.executeUpdate();
            if (query > 0) {
                JOptionPane.showMessageDialog(this, "Update Successfully", "Success", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Fail", "Fail", JOptionPane.ERROR_MESSAGE);

            }
            con.close();
        } catch (Exception e) {
            System.out.println(e);
        }
        fieldClean();
    }//GEN-LAST:event_jButton2ActionPerformed

